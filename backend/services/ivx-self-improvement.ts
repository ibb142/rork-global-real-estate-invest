// ── Validation ─────────────────────────────────────────────────────────────

export async function validateSelfImprovement(): Promise<{ valid: boolean; issues: string[] }> {
  const state = await loadState();
  const issues: string[] = [];
  if (state.marker !== IVX_SELF_IMPROVEMENT_MARKER) issues.push('State marker mismatch');
  return { valid: issues.length === 0, issues };
}
