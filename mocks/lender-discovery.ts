export const getDiscoveredByCategory = (category: LenderCategory | 'all'): DiscoveredLender[] => {
  if (category === 'all') return discoveredLenders;
  return discoveredLenders.filter(l => l.category === category);
};

export const getDiscoveredByRegion = (region: string): DiscoveredLender[] => {
  if (region === 'Global') return discoveredLenders;
  return discoveredLenders.filter(l => l.preferredRegions.some(r => r === region || r === 'Global'));
};

export const getDiscoveredBySource = (source: DiscoveredLender['source']): DiscoveredLender[] => {
  return discoveredLenders.filter(l => l.source === source);
};

export const getDiscoveredByCountry = (country: string): DiscoveredLender[] => {
  return discoveredLenders.filter(l => l.country.toLowerCase() === country.toLowerCase());
};
