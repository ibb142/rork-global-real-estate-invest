export function getEmailLogStats() {
  const total = emailLogs.length;
  const automatic = emailLogs.filter(l => l.type === 'automatic').length;
  const manual = emailLogs.filter(l => l.type === 'manual').length;
  const delivered = emailLogs.filter(l => l.status === 'delivered' || l.status === 'opened' || l.status === 'clicked' || l.status === 'replied').length;
  const opened = emailLogs.filter(l => l.status === 'opened' || l.status === 'clicked' || l.status === 'replied').length;
  const clicked = emailLogs.filter(l => l.status === 'clicked' || l.status === 'replied').length;
  const replied = emailLogs.filter(l => l.status === 'replied').length;
  const bounced = emailLogs.filter(l => l.status === 'bounced').length;
  const failed = emailLogs.filter(l => l.status === 'failed').length;

  return { total, automatic, manual, delivered, opened, clicked, replied, bounced, failed };
}
